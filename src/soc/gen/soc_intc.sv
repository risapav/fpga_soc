/**
 * @file soc_intc.sv
 * @brief Vectored Interrupt Controller (VIC) with Priority Encoder
 * @details This module aggregates peripheral interrupts, masks them, 
 * and provides a prioritized Interrupt ID for the CPU via a Bus interface.
 * Compatible with Intel Quartus Prime 25.1 Lite.
 */

`default_nettype none

module soc_intc #(
  parameter int NUM_IRQ = 32
) (
  input  wire         clk_i,
  input  wire         rst_ni,
  
  // Peripheral Interrupt Vector
  input  wire [NUM_IRQ-1:0] irqs_i,
  
  // CPU Interrupt Line (Level-triggered)
  output wire         cpu_irq,
  
  // Bus Interface (Slave)
  bus_if.slave        bus
);

  // --- Internal Registers ---
  logic [NUM_IRQ-1:0] enable_reg;   // Masking register (RW)
  logic [NUM_IRQ-1:0] pending_reg;  // Masked status (RO)
  logic [4:0]         claimed_id;   // Last claimed ID (RO)

  // --- Interrupt Logic ---
  // Only interrupts that are enabled contribute to pending status
  assign pending_reg = irqs_i & enable_reg;
  
  // CPU is signaled if ANY enabled interrupt is active
  assign cpu_irq = |pending_reg;

  // --- Priority Encoder (Fixed Priority: Highest Index = Highest Priority) ---
  logic [4:0] current_id;
  
  always_comb begin
    current_id = 5'd0;
    // unique casez allows Quartus to optimize for a priority tree
    unique casez (pending_reg)
      32'b1???????????????????????????????: current_id = 5'd31;
      32'b01??????????????????????????????: current_id = 5'd30;
      32'b001?????????????????????????????: current_id = 5'd29;
      32'b0001????????????????????????????: current_id = 5'd28;
      32'b00001???????????????????????????: current_id = 5'd27;
      32'b000001??????????????????????????: current_id = 5'd26;
      32'b0000001?????????????????????????: current_id = 5'd25;
      32'b00000001????????????????????????: current_id = 5'd24;
      32'b000000001???????????????????????: current_id = 5'd23;
      32'b0000000001??????????????????????: current_id = 5'd22;
      32'b00000000001?????????????????????: current_id = 5'd21;
      32'b000000000001????????????????????: current_id = 5'd20;
      32'b0000000000001???????????????????: current_id = 5'd19;
      32'b00000000000001??????????????????: current_id = 5'd18;
      32'b000000000000001?????????????????: current_id = 5'd17;
      32'b0000000000000001????????????????: current_id = 5'd16;
      32'b00000000000000001???????????????: current_id = 5'd15;
      32'b000000000000000001??????????????: current_id = 5'd14;
      32'b0000000000000000001?????????????: current_id = 5'd13;
      32'b00000000000000000001????????????: current_id = 5'd12;
      32'b000000000000000000001???????????: current_id = 5'd11;
      32'b0000000000000000000001??????????: current_id = 5'd10;
      32'b00000000000000000000001?????????: current_id = 5'd9;
      32'b000000000000000000000001????????: current_id = 5'd8;
      32'b0000000000000000000000001???????: current_id = 5'd7;
      32'b00000000000000000000000001??????: current_id = 5'd6;
      32'b000000000000000000000000001?????: current_id = 5'd5;
      32'b0000000000000000000000000001????: current_id = 5'd4;
      32'b00000000000000000000000000001???: current_id = 5'd3;
      32'b000000000000000000000000000001??: current_id = 5'd2;
      32'b0000000000000000000000000000001?: current_id = 5'd1;
      32'b00000000000000000000000000000001: current_id = 5'd0;
      default: current_id = 5'd0;
    endcase
  end

  // --- Bus Interface Logic ---
  // Address offsets:
  // 0x00: ENABLE register
  // 0x04: PENDING status (Read-only)
  // 0x08: CLAIM register (Read to get current ID)
  // 0x0C: COMPLETE register (Write to clear)
  
  always_ff @(posedge clk_i or negedge rst_ni) begin
    if (!rst_ni) begin
      enable_reg  <= '0;
      claimed_id  <= '0;
      bus.ready   <= 1'b0;
      bus.rdata   <= 32'h0;
    end else begin
      bus.ready <= bus.valid; // Zero-wait-state bus response
      
      // Write Path
      if (bus.valid && bus.we) begin
        case (bus.addr[3:0])
          4'h0: enable_reg <= bus.wdata;
          4'hC: begin
            // Complete logic could trigger ACK signals back to peripherals 
            // if they were edge-triggered. For level, it's just a placeholder.
          end
          default: ;
        endcase
      end

      // Read Path
      if (bus.valid && !bus.we) begin
        case (bus.addr[3:0])
          4'h0: bus.rdata <= enable_reg;
          4'h4: bus.rdata <= pending_reg;
          4'h8: begin
            bus.rdata  <= {27'h0, current_id};
            claimed_id <= current_id; // Latch claimed ID
          end
          default: bus.rdata <= 32'hDEADBEEF;
        endcase
      end
    end
  end

endmodule : soc_intc

`default_nettype wire
